    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
        if(jtNombre.getText().isEmpty()||jtPrecio.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No deje vacio los campos");
        }else{
            try {
                
                double precio=Double.parseDouble(jtPrecio.getText());
                String categoria=jcbCategoria.getSelectedItem().toString();
                Producto prod=new Producto(categoria,jtNombre.getText(),precio);
                cargarDatos(prod);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Introdusca un numero en precio");
            }
        }
        LimpiarPantalla();
    }//GEN-LAST:event_jbAgregarActionPerformed

