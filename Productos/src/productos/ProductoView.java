    private void armarCabecera(){
        modelo.addColumn("Producto");
        modelo.addColumn("Categoria");
        modelo.addColumn("Precio");
        jtTablaProductos.setModel(modelo);
    }
    
    private void cargarCombo(){
        jcbCategoria.addItem(new Productos("Electronica"));
        jcbCategoria.addItem(new Productos("Ropa"));
        jcbCategoria.addItem(new Productos("Alimentos"));
        
      
    }
    
    private void cargarDatos(Producto pr){
        modelo.addRow(new Object[]{pr.getNombre(),pr.getCategoria(),pr.getPrecio()});
    }
    public void LimpiarPantalla(){
     jtNombre.setText("");
     jtPrecio.setText("");
    
    }

